package model;

public class DateTimeDiff {

	private int secondsDiff=0;
	private int minutesDiff=0;
	private int hourDiffe=0;
	private int daysDiff=0;
	private int monthsDiff=0;
	private int yearsDiff=0;
	
	public DateTimeDiff() {
		
	}

	public int getSecondsDiff() {
		return secondsDiff;
	}

	public void setSecondsDiff(int secondsDiff) {
		this.secondsDiff = secondsDiff;
	}

	public int getMinutesDiff() {
		return minutesDiff;
	}

	public void setMinutesDiff(int minutesDiff) {
		this.minutesDiff = minutesDiff;
	}

	public int getHourDiffe() {
		return hourDiffe;
	}

	public void setHourDiffe(int hourDiffe) {
		this.hourDiffe = hourDiffe;
	}

	public int getDaysDiff() {
		return daysDiff;
	}

	public void setDaysDiff(int daysDiff) {
		this.daysDiff = daysDiff;
	}

	public int getMonthsDiff() {
		return monthsDiff;
	}

	public void setMonthsDiff(int monthsDiff) {
		this.monthsDiff = monthsDiff;
	}

	public int getYearsDiff() {
		return yearsDiff;
	}

	public void setYearsDiff(int yearsDiff) {
		this.yearsDiff = yearsDiff;
	}
	
}